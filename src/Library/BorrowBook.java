    private void Al() {
        String kname = txtKitapName.getText();
        String kadi = txtUsername.getText();
        int c = 0;
        int m = 0;
        
        PreparedStatement pst2;
        ResultSet rs2;
        PreparedStatement pst3;
        ResultSet rs3;
        try {
            pst2 = DbConnect.con.prepareStatement("select * from books");
            pst3 = DbConnect.con.prepareStatement("select * from student");
            rs2 = pst2.executeQuery();
            rs3 = pst3.executeQuery();
            while(rs2.next()){
                if((rs2.getString("name").equals(kname))){
                       c++;
                    }
            }
            rs2.close();
            pst2.close();
            while (rs3.next()) {
                        
                        if(rs3.getString("username").equals(kadi)){
                            m++;
                        }
                }
            rs3.close();
            pst3.close();
            if(c==0 || m==0){
                     JOptionPane.showMessageDialog(null, "Kullanıcı adı veya kitap adı hatalı");
            } else {
                try {
                            pst = DbConnect.con.prepareStatement("insert into borrow(kitapname, username) values (?,?)");
                            pst.setString(1, kname);
                            pst.setString(2, kadi);

                            int k = pst.executeUpdate();

                            if(k==1){
                                JOptionPane.showMessageDialog(this, "Kitap ödünç Alındı");
                                c=0;
                                m=0;
                                Temizle();
                            }
                        } catch (SQLException e) {
                            if(e.toString().contains("kitapname"))
                                JOptionPane.showMessageDialog(this, "Bu kitap şuan başka bir kullanıcıda. Lütfen iade etmesini bekleyiniz");
                            else if(e.toString().contains("username"))
                                JOptionPane.showMessageDialog(null, "Bu kullanıcının iade etmediği kitap bulunmaktadır. En fazla 1 adet kitap alınabilir");
                            else
                                JOptionPane.showMessageDialog(null, e);
                        }
            }
            rs.close();
            pst.close();
        } catch (SQLException ee) {
            JOptionPane.showMessageDialog(null, ee);
        }
    }
